/// POST /practice-questions - Generate practice questions
pub async fn generate_practice_questions(
    State(state): State<AppState>,
    Json(body): Json<GenerateQuestionsRequest>,
) -> Result<Json<ApiResponse<serde_json::Value>>, (StatusCode, String)> {
    let count = body.count.unwrap_or(5);
    let difficulty = body.difficulty.clone().unwrap_or(Difficulty::Medium);
    
    match state.ai_service.generate_practice_questions(
        &body.course_id,
        &body.topic,
        count,
        difficulty,
    ).await {
        Ok(questions) => Ok(Json(ApiResponse::success(serde_json::json!({
            "questions": questions,
            "topic": body.topic,
            "count": questions.len()
        })))),
        Err(e) => Err((StatusCode::INTERNAL_SERVER_ERROR, e.to_string())),
    }
}

/// POST /summarize - Summarize a course module
pub async fn summarize_module(
    State(state): State<AppState>,
    Json(body): Json<SummarizeModuleRequest>,
) -> Result<Json<ApiResponse<serde_json::Value>>, (StatusCode, String)> {
    match state.ai_service.summarize_module(&body.course_id, &body.module_id).await {
        Ok(summary) => Ok(Json(ApiResponse::success(serde_json::json!({
            "module_id": body.module_id,
            "course_id": body.course_id,
            "summary": summary
        })))),
        Err(e) => Err((StatusCode::INTERNAL_SERVER_ERROR, e.to_string())),
    }
}

/// GET /search - Search for relevant course content
#[derive(Debug, Deserialize)]
pub struct SearchQuery {
    pub course_id: String,
    pub query: String,
    pub limit: Option<usize>,
}

pub async fn search_content(
    State(state): State<AppState>,
    query: axum::extract::Query<SearchQuery>,
) -> Result<Json<ApiResponse<serde_json::Value>>, (StatusCode, String)> {
    let limit = query.limit.unwrap_or(10);
    
    match state.ai_service.search_relevant_content(&query.course_id, &query.query, limit).await {
        Ok(chunks) => Ok(Json(ApiResponse::success(serde_json::json!({
            "results": chunks,
            "count": chunks.len()
        })))),
        Err(e) => Err((StatusCode::INTERNAL_SERVER_ERROR, e.to_string())),
    }
}

/// Create AI assistant router
pub fn ai_router() -> Router<AppState> {
    Router::new()
        .route("/ask", post(ask_question))
        .route("/index", post(index_content))
        .route("/recommendations", post(get_recommendations))
        .route("/practice-questions", post(generate_practice_questions))
        .route("/summarize", post(summarize_module))
        .route("/search", get(search_content))
}
