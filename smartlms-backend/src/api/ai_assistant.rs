use axum::{
    extract::State,
    http::StatusCode,
    response::Json,
    routing::{get, post},
    Router,
};
use serde::{Deserialize, Serialize};
use crate::services::ai_learning::*;
use crate::services::ai_learning_impl::AiLearningServiceImpl;
use std::sync::Arc;

/// Shared application state containing the AI service
#[derive(Clone)]
pub struct AppState {
    pub ai_service: Arc<AiLearningServiceImpl>,
}

/// Request to ask a question to the AI assistant
#[derive(Debug, Deserialize)]
pub struct AskQuestionRequest {
    pub course_id: String,
    pub question: String,
    pub conversation_history: Option<Vec<Message>>,
}

/// Request to index course content
#[derive(Debug, Deserialize)]
pub struct IndexContentRequest {
    pub course_id: String,
    pub chunks: Vec<ContentChunkInput>,
}

#[derive(Debug, Deserialize)]
pub struct ContentChunkInput {
    pub module_id: Option<String>,
    pub content_type: ContentType,
    pub title: String,
    pub text: String,
    pub metadata: Option<ChunkMetadata>,
}

/// Request to get study recommendations
#[derive(Debug, Deserialize)]
pub struct GetRecommendationsRequest {
    pub course_id: String,
}

/// Request to generate practice questions
#[derive(Debug, Deserialize)]
pub struct GenerateQuestionsRequest {
    pub course_id: String,
    pub topic: String,
    pub count: Option<usize>,
    pub difficulty: Option<Difficulty>,
}

/// Request to summarize a module
#[derive(Debug, Deserialize)]
pub struct SummarizeModuleRequest {
    pub course_id: String,
    pub module_id: String,
}

/// API Response wrapper
#[derive(Debug, Serialize)]
pub struct ApiResponse<T> {
    pub success: bool,
    pub data: Option<T>,
    pub error: Option<String>,
}

impl<T: Serialize> ApiResponse<T> {
    pub fn success(data: T) -> Self {
        Self {
            success: true,
            data: Some(data),
            error: None,
        }
    }
    
    pub fn error(message: String) -> Self {
        Self {
            success: false,
            data: None,
            error: Some(message),
        }
    }
}

/// POST /ask - Ask a question to the AI learning assistant
pub async fn ask_question(
    State(state): State<AppState>,
    Json(body): Json<AskQuestionRequest>,
) -> Result<Json<ApiResponse<AssistantResponse>>, (StatusCode, String)> {
    let query = AssistantQuery {
        user_id: "current_user".to_string(), // Would come from auth context
        course_id: body.course_id.clone(),
        question: body.question.clone(),
        conversation_history: body.conversation_history.clone().unwrap_or_default(),
        context_limit: None,
    };
    
    match state.ai_service.answer_question(query).await {
        Ok(response) => Ok(Json(ApiResponse::success(response))),
        Err(e) => Err((StatusCode::INTERNAL_SERVER_ERROR, e.to_string())),
    }
}

/// POST /index - Index course content for semantic search
pub async fn index_content(
    State(state): State<AppState>,
    Json(body): Json<IndexContentRequest>,
) -> Result<Json<ApiResponse<serde_json::Value>>, (StatusCode, String)> {
    // Convert input chunks to full ContentChunk objects
    let chunks: Vec<ContentChunk> = body
        .chunks
        .iter()
        .map(|input| ContentChunk {
            id: uuid::Uuid::new_v4().to_string(),
            course_id: body.course_id.clone(),
            module_id: input.module_id.clone(),
            content_type: input.content_type.clone(),
            title: input.title.clone(),
            text: input.text.clone(),
            embedding: vec![], // Will be generated by the service
            metadata: input.metadata.clone().unwrap_or_default(),
            created_at: chrono::Utc::now(),
        })
        .collect();
    
    match state.ai_service.index_course_content(&body.course_id, chunks).await {
        Ok(count) => Ok(Json(ApiResponse::success(serde_json::json!({
            "indexed_count": count,
            "course_id": body.course_id
        })))),
        Err(e) => Err((StatusCode::INTERNAL_SERVER_ERROR, e.to_string())),
    }
}

